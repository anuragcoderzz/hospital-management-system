    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // Coding for search
        pid= txtpid.getText();
        if (!pid.equals("")) {
            try {
                con = connection.connect.conn();
                ps = con.prepareStatement("select * from patient_detail where pid=?");
                ps.setString(1, pid);
                rs = ps.executeQuery();

                while (rs.next()) {
                    txtpid.setText(rs.getString(1).trim());
                    txtfname.setText(rs.getString(2).trim());
                    txtlname.setText(rs.getString(3).trim());
                    txtage.setText(rs.getString(4).trim());
                    txtsex.setText(rs.getString(5).trim());
                    txtadd.setText(rs.getString(6).trim());
                    txtstate.setText(rs.getString(7).trim());
                    txtpin.setText(rs.getString(8).trim());
                    txtph.setText(rs.getString(9).trim());
                    txtdob.setText(rs.getString(10).trim());
                    }

            } catch (Exception e) {
                e.printStackTrace();
               }

        } else {
            JOptionPane.showMessageDialog(null, "Please Enter the Patient Id!!");
        }
    }//GEN-LAST:event_btnsearchActionPerformed


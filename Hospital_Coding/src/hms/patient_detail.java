    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // coding for save
        pid= txtpid.getText();
        fname= txtfname.getText();
        lname= txtlname.getText();
        age=txtage.getText();
        sex=txtsex.getText();
        address=txtadd.getText();
        state=txtstate.getText();
        pin=txtpin.getText();
        ph=txtph.getText();
        dob=txtdob.getText();
        if (!pid.equals("")) {
            try {
                con=connection.connect.conn();
                ps = con.prepareStatement("insert into patient_detail values(?,?,?,?,?,?,?,?,?,?)");
                ps.setString(1, pid);
                ps.setString(2, fname);
                ps.setString(3, lname);
                ps.setString(4, age);
                ps.setString(5, sex);
                ps.setString(6, address);
                ps.setString(7, state);
                ps.setString(8,pin);
                ps.setString(9,ph);
                ps.setString(10, dob);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data has been inserted successfully ");
                clear();
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Data has not been inserted!!");
        }
    }//GEN-LAST:event_btnsaveActionPerformed


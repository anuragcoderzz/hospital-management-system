        // TODO add your handling code here:
         eid= txteid.getText();
        ename= txtename.getText();
        post= txtpost.getText();
        salary=txtsalary.getText();
        age=txtage.getText();
        sex=txtsex.getText();
        address=txtadd.getText();
        state=txtstate.getText();
        pin=txtpin.getText();
        ph=txtph.getText();
        if (!eid.equals("")) {
            try {
                con=connection.connect.conn();
                ps = con.prepareStatement("insert into emp_detail values(?,?,?,?,?,?,?,?,?,?)");
                ps.setString(1, eid);
                ps.setString(2, ename);
                ps.setString(3, post);
                ps.setString(4, salary);
                ps.setString(5, age);
                ps.setString(6, sex);
                ps.setString(7, address);
                ps.setString(8,state);
                ps.setString(9,pin);
                ps.setString(10, ph);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data has been inserted successfully ");
                clear();
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Data has not been inserted!!");
        }

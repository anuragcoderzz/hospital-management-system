    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        eid= txteid.getText();
        if (!eid.equals("")) {
            try {
                con = connection.connect.conn();
                ps = con.prepareStatement("select * from emp_detail where eid=?");
                ps.setString(1, eid);
                rs = ps.executeQuery();

                while (rs.next()) {
                    txteid.setText(rs.getString(1).trim());
                    txtename.setText(rs.getString(2).trim());
                    txtpost.setText(rs.getString(3).trim());
                    txtsalary.setText(rs.getString(4).trim());
                    txtage.setText(rs.getString(5).trim());
                    txtsex.setText(rs.getString(6).trim());
                    txtadd.setText(rs.getString(7).trim());
                    txtstate.setText(rs.getString(8).trim());
                    txtpin.setText(rs.getString(9).trim());
                    txtph.setText(rs.getString(10).trim());
                    }

            } catch (Exception e) {
                e.printStackTrace();
               }

        } else {
            JOptionPane.showMessageDialog(null, "Please Enter the Employee Id!!");
        }
    }//GEN-LAST:event_btnsearchActionPerformed

